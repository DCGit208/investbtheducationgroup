            navLinks.forEach(l=> l.removeAttribute('aria-current'));
            const id = '#' + en.target.id;
            const link = navLinks.find(l=> l.getAttribute('href')===id);
            if(link){ link.setAttribute('aria-current','true'); }
          }
        });
      }, { rootMargin:'-40% 0px -50% 0px', threshold:0.2 });
      sections.forEach(s=> io.observe(s));
    }
    // Count-up animation (data-countup or data-target)
    function runCount(el){
      const target = parseInt(el.dataset.countup || el.dataset.target,10); if(!target||isNaN(target)) return;
      const suffix = el.dataset.suffix || ''; const dur=1200; const start=performance.now();
      function step(ts){ const p=Math.min((ts-start)/dur,1); const val=Math.floor(p*target); el.textContent=val.toLocaleString()+suffix; if(p<1) requestAnimationFrame(step); }
      requestAnimationFrame(step);
    }
    const statEls = document.querySelectorAll('.stat-value[data-countup], .stat-value[data-target]');
    if(statEls.length){
      const hero = statEls[0].closest('.hero') || document.body;
      const seenObserver = new IntersectionObserver(e=>{ if(e[0].isIntersecting){ statEls.forEach(runCount); seenObserver.disconnect(); } }, {threshold:0.3});
      seenObserver.observe(hero);
    }
